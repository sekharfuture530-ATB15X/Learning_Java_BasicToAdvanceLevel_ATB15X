package ex_Lab006_Switch_Statments;

import java.util.Scanner;

public class Lab048_Swtch_Case {
    public static void main(String[] args) {

        Scanner scanner = new Scanner(System.in);
        System.out.println("Enter the number from 1 to 7 for the day program");

        if(scanner.hasNextInt()) {


            int day = scanner.nextInt();
            switch (day) {
                case 1:
                    System.out.println("Mon");
                    break;
                case 2:
                    System.out.println("Tue");
                    break;
                case 3:
                    System.out.println("Wed");
                    break;
                case 4:
                    System.out.println("Thur");
                    break;
                case 5:
                    System.out.println("Fri");
                    break;
                case 6:
                    System.out.println("Sat");
                    break;
                case 7:
                    System.out.println("Sun");
                    break;
                default:
                    System.out.println("Please enter valid number from 1 to 7");
                    break;

            }
        }

        else{
            System.out.println("Invalid int is entered!");
        }
        System.out.println("End of program");
    }
}

