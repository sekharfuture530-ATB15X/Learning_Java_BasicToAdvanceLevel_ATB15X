package ex_Lab004_Operators;

public class Lab029_Logical_Operator {

    public static void main(String[] args) {

//        Logical Operators like AND, OR & NOT

        boolean a = true;
        boolean b = false;
        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println(!a);
    }
}
