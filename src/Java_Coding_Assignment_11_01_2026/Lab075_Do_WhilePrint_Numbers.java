//9) Print Numbers Using do-while
//
//Description:
//Write a Java program to print numbers from 1 to 10 using a do-while loop.

public class Lab075_Do_WhilePrint_Numbers {
    public static void main(String[] args) {


        int i =1;
        do{
            System.out.println(i);
            i++;
        }while(i<=10);
    }
}
