//11) Reverse a Number
//
//Description:
//Write a Java program to reverse a given number using a do-while loop.

public class Lab077_Do_While_ReverseNumbers {
    public static void main(String[] args) {

        int i = 10;
        do{
            System.out.println(i);
//            System.out.print(i+ " ");
            i--;

        }while (i>0);
    }
}
