package ex_Lab005_TypeCasting;

public class Lab037_Char_int {
//Widening Casting (Implicit / Automatic)-> Smaller data type to larger data type for example:int to double or char to int
    public static void main(String[] args) {

        char ch = 'A';
        int a = ch;

        System.out.println(ch);
        System.out.println(a);
    }
}
