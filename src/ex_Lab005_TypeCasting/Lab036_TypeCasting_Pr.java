package ex_Lab005_TypeCasting;

// this is int to double data type
public class Lab036_TypeCasting_Pr {
// Type Casting means converting from one data type into another data type
//    Primitive data type like, byte,short,int,long,float,double and char, boolean

    public static void main(String[] args) {

        int a = 100;
        double b = a; // automatic typecasting
        System.out.println(b);
        System.out.println(a);


    }
}
